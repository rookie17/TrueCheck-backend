# ─── Manual retrain trigger ───────────────────────────────────────────────────

@app.route("/retrain", methods=["POST"])
def retrain():
    """Manually trigger a model retrain. Useful after bulk importing products."""
    try:
        from ml.train import train
        from ml.predict import _load_model
        result = train()
        _load_model()   # hot-reload without restarting server
        return jsonify({"status": "ok", "result": str(result)})
    except Exception as e:
        logger.exception("Manual retrain failed")
        return jsonify({"error": str(e)}), 500


