@app.route("/get-complete-product-info", methods=["GET"])
def get_complete_product_info():
    barcode = request.args.get("barcode")
    if not barcode:
        return jsonify({"error": "No barcode provided"}), 400

    product_data   = get_product_from_db(barcode)
    nutrition_data = {}

    # ── FIX #6: Early return if fully cached (skips ML re-run + DB write) ─────
    if (product_data
            and product_data.get("product_rating")
            and product_data.get("nutrients_per_100g")):

        logger.info("Full cache hit for %s — returning saved rating", barcode)

        ingredient_names      = _flatten_ingredients(product_data["ingredients"])
        final_ingredient_list = []
        for name in ingredient_names:
            profile = get_ingredient_profile_from_db(name)
            if not profile:
                profile = _build_ingredient_profile_fallback(name)
            final_ingredient_list.append({"name": name, "profile": _fix_nested_profile(profile)})

        return jsonify({
            "product_name":        product_data["product_name"],
            "ingredients_profile": final_ingredient_list,
            "nutrients":           product_data["nutrients_per_100g"],
            "percent_estimate":    product_data.get("percent_estimate", []),
            "overall_rating":      _build_rating_response(product_data["product_rating"]),
            "cached":              True,
        })

    # ── Partial cache: has nutrients but no rating yet ────────────────────────
    if product_data and product_data.get("nutrients_per_100g"):
        logger.info("Partial cache hit for %s — rating missing, running ML", barcode)
        product_name     = product_data["product_name"]
        ingredient_names = _flatten_ingredients(product_data["ingredients"])
        nutrition_data   = product_data["nutrients_per_100g"]

    else:
        # ── Full cache miss: fetch from OpenFoodFacts ─────────────────────────
        logger.info("Cache miss — fetching from OpenFoodFacts for %s", barcode)

        url     = f"https://world.openfoodfacts.org/api/v0/product/{barcode}.json"
        headers = {"User-Agent": "TrueCheck-App/1.0 (student project)"}

        MAX_RETRIES  = 3
        RETRY_DELAYS = [3, 8, 20]
        resp         = None

        for attempt, delay in enumerate(RETRY_DELAYS):
            try:
                resp = requests.get(url, headers=headers, timeout=10)
                logger.info("Attempt %d — status: %d", attempt + 1, resp.status_code)

                if resp.status_code == 200:
                    break

                if resp.status_code == 429:
                    if attempt < MAX_RETRIES - 1:
                        logger.warning("Rate limited. Waiting %ds ...", delay)
                        time.sleep(delay)
                        continue
                    return jsonify({
                        "error": "OpenFoodFacts rate limit reached after retries.",
                        "tip":   "Scan again in a minute — first scan is cached.",
                    }), 429

                return jsonify({"error": f"OpenFoodFacts error {resp.status_code}"}), 500

            except requests.exceptions.Timeout:
                logger.warning("Timeout on attempt %d", attempt + 1)
                if attempt < MAX_RETRIES - 1:
                    time.sleep(delay)
                else:
                    return jsonify({"error": "OpenFoodFacts timed out after retries."}), 504

            except Exception as e:
                logger.exception("Unexpected error fetching from OpenFoodFacts")
                return jsonify({"error": str(e)}), 500

        try:
            product = resp.json().get("product", {})

            if not product:
                return jsonify({
                    "error":   "Product not found",
                    "message": "This barcode is not in the OpenFoodFacts database.",
                    "barcode": barcode,
                }), 404

            product_name     = product.get("product_name", "Unknown")
            raw_ingredients  = [
                i.get("text") for i in product.get("ingredients", []) if "text" in i
            ]
            ingredient_names = _flatten_ingredients(raw_ingredients)
            nutriments       = product.get("nutriments", {})
            nutrition_data   = {k: v for k, v in nutriments.items() if k.endswith("_100g")}

            if not nutrition_data and not ingredient_names:
                return jsonify({
                    "error":        "No food data found",
                    "message":      "This may not be a food product.",
                    "product_name": product_name,
                }), 404

            save_product_to_db(barcode, product_name, ingredient_names, nutrition_data)

        except Exception as e:
            logger.exception("Failed to parse OpenFoodFacts response")
            return jsonify({"error": str(e)}), 500

    # ── Ingredient profiles ───────────────────────────────────────────────────
    ingredient_names      = _flatten_ingredients(ingredient_names)
    final_ingredient_list = []

    for name in ingredient_names:
        if not isinstance(name, str):
            continue
        profile = get_ingredient_profile_from_db(name)

        if not profile:
            # FIX #5: fallback matches old Gemini shape — Firestore stays consistent
            profile = _build_ingredient_profile_fallback(name)
            save_ingredient_to_db(name, name, profile)

        final_ingredient_list.append({"name": name, "profile": _fix_nested_profile(profile)})

    # ── Percent estimate ──────────────────────────────────────────────────────
    percent_estimates = get_percent_estimates(barcode, ingredient_names)

    # ── ML Prediction ─────────────────────────────────────────────────────────
    product_for_ml = {
        "ingredients":        ingredient_names,
        "nutrients_per_100g": nutrition_data,
    }

    rating          = predict_score(product_for_ml)
    rating_response = _build_rating_response(rating)  # FIX #3: schema wrapper

    # ── Save ──────────────────────────────────────────────────────────────────
    save_product_rating_to_db(barcode, rating)
    save_percent_estimate_to_db(barcode, percent_estimates)

    return jsonify({
        "product_name":        product_name,
        "ingredients_profile": final_ingredient_list,
        "nutrients":           nutrition_data,
        "percent_estimate":    percent_estimates,
        "overall_rating":      rating_response,
        "cached":              False,
    })


