# ─── Helpers ──────────────────────────────────────────────────────────────────

def _flatten_ingredients(raw_list: list) -> list:
    """Normalise a mixed list of strings / dicts to plain strings."""
    result = []
    for item in raw_list:
        if isinstance(item, dict):
            name = item.get("text") or item.get("name") or ""
        else:
            name = str(item)
        name = name.strip().lower()
        if name:
            result.append(name)
    return result


def _build_rating_response(rating: dict) -> dict:
    """
    Maps the ML rating dict to a shape that satisfies BOTH:
      - Old frontend schema: { product_score, pros, cons, dietary_tags,
                               processing_level, why_this_score }
      - New ML schema:       { overall_score, method, confidence, breakdown }

    This means the frontend keeps working without any changes,
    while new clients can also read the richer ML fields.
    """
    score     = rating.get("overall_score", 0)
    breakdown = rating.get("breakdown", {})
    method    = rating.get("method", "rule_based")
    confidence = rating.get("confidence", "medium")

    # Map 0-10 score to letter grade for legacy processing_level field
    if score >= 8:
        grade = "A"
    elif score >= 6:
        grade = "B"
    elif score >= 4:
        grade = "C"
    elif score >= 2:
        grade = "D"
    else:
        grade = "F"

    return {
        # ── Old keys — frontend keeps working without changes ─────────────────
        "product_score":     round(score, 2),
        "processing_level":  grade,
        "why_this_score":    (
            f"Score generated by ML model (method: {method}, "
            f"confidence: {confidence})."
        ),
        "pros":              breakdown.get("positives", []),
        "cons":              breakdown.get("negatives", []),
        "dietary_tags":      [],   # reserved for future use

        # ── New ML keys — richer data for updated frontend ────────────────────
        "overall_score":     round(score, 2),
        "method":            method,
        "confidence":        confidence,
        "breakdown":         breakdown,
    }


def _build_ingredient_profile_fallback(name: str) -> dict:
    """
    Rule-based fallback profile that matches the OLD Gemini profile shape
    so Firestore stays consistent across cached and new ingredients.
    """
    return {
    "ingredient_name": name,
    "ingredient_profile": {
        "health_score":       5,
        "pros":               [],
        "cons":               [],
        "allergen":           "unknown",
        "regulatory_status":  "unknown",
        "category":           "other",
        "health_flag":        "unknown",
        "source":             "rule_based",
        "description":        "",
        "dietary_tags":       [],
        "flags":              [],
        "processing_level":   "unknown",
        "why_this_score":     "No profile available — rule-based fallback.",
    },
}
def _fix_nested_profile(profile: dict) -> dict:
    """Fix double-nested ingredient profiles from old rule-based saves."""
    if not profile:
        return profile
    inner = profile.get("ingredient_profile", {})
    if isinstance(inner, dict) and "ingredient_profile" in inner:
        profile["ingredient_profile"] = inner["ingredient_profile"]
    return profile


